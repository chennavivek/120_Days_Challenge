# Tuple Functions

def min_in_tuple(tpl):
    return min(tpl)
def max_in_tuple(tpl):
    return max(tpl)
def sum_in_tuple(tpl):
    return sum(tpl)

sample_tpl = (1, 2, 3, 4, 5)
print(f"Minimum in tuple: {min_in_tuple(sample_tpl)}")
print(f"Maximum in tuple: {max_in_tuple(sample_tpl)}")
print(f"Sum of elements in tuple: {sum_in_tuple(sample_tpl)}")